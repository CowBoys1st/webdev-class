// Data Types

// Strings / text
// Menggunakan kutip saatu atau kutip dua
var greet = "Hello"
var morning = 'Good Morning'
var oneFour = '1234'

// Numbers / angka

// Integer , bilangan bulat
var intOne = 123
var intTwo = 345
var intThree = 12

// Float , bilangan desimal
var twentyThree = 0.23
var pi = 3.14
var perfect = 8.99

// Boolean
// Hanya memmiliki dua nilaii true dan false

var graduate = true
var sick = false

// Object
// menyimpan data lebih dari satu, object dapat memiliki "variabel"

var name = {firstName : "Tanjirou", lastName : "Kamado"}

console.log(name.firstName)
console.log(name.lastName)

// Array
// Menyimpan data lebih dari satu, setiap data akan diwakilkan oleh nomor index

var days = ["Sunday", "Monday", "Tuesday"]

console.log(days[0])
console.log(days[1])
console.log(days[2])

// Undifined dan null
// Mewakili variabel yang tidak menyimpan nilai apapun

var undfnd
console.log(undfnd)

var nll = null
console.log(nll)

// Typeof
// Mencari tahu jenis tipe data

var lang = "Javascript"

var result = typeof(lang)
console.log(result)

